
# 12 Cree un programa donde se asigne un valor a una variable de tipo int y otra de 
# tipo float, sumarlas y mostrar el resultado, indicando también el tipo de dato del resultado.

entero = int(input("Ingrese un valor entero: "))
flotante = float(input("Ingrese un valor real: "))

print(f"Valor entero ingresado: {entero} de tipo: {type(entero)}")
print(f"Valor real ingresado: {flotante} de tipo: {type(flotante)}")